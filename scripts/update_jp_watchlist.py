ROOT = Path(__file__).resolve().parent.parent
CFG = ROOT / "config" / "default.example.json"


def main():
    cfg = json.loads(CFG.read_text(encoding="utf-8"))
    existing_names = cfg.get("watchlist_names", {})
    existing_aliases = cfg.get("watchlist_aliases", {})

    new_watchlist = []
    new_names = {}
    kept, translated, english_only = 0, 0, 0
    english_only_tickers = []
    for code, english_name in JPX400_RAW:
        ticker = f"{code}.T"
        new_watchlist.append(ticker)
        # Precedence: hand-curated existing name > JP translation > English PDF fallback.
        if ticker in existing_names and existing_names[ticker].strip():
            new_names[ticker] = existing_names[ticker]
            kept += 1
        elif code in JP_TRANSLATIONS:
            new_names[ticker] = JP_TRANSLATIONS[code]
            translated += 1
        else:
            new_names[ticker] = english_name
            english_only += 1
            english_only_tickers.append(ticker)

    cfg["watchlist"] = new_watchlist
    cfg["watchlist_names"] = new_names
    if existing_aliases:
        cfg["watchlist_aliases"] = existing_aliases

    CFG.write_text(json.dumps(cfg, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

    print(f"Updated {CFG}")
    print(f"  Total tickers: {len(new_watchlist)}")
    print(f"  Kept existing JP name:          {kept}")
    print(f"  Added JP translation:           {translated}")
    print(f"  Still English-only:             {english_only}")
    if english_only_tickers:
        print(f"  English-only tickers: {english_only_tickers}")




if __name__ == "__main__":
    main()
